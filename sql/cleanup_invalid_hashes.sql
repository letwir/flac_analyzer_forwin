-- 誤った audio_hash で登録されてしまったレコードを削除するためのSQLスクリプトですわ。
-- `audio_hash` 自体は常に32文字のMD5文字列となるため、文字列パターンで旧仕様のレコードを見分けることはできませんの。
-- したがって、前回の実装以降（例：2026-07-01以降）に処理されたレコードを `analyzed_at` や `changed_at` を基準に一括削除し、
-- オーケストレーターで再度解析し直す運用を推奨いたしますわ。

BEGIN;

-- 1. まずは履歴テーブル（トリガーにより退避されたもの）から、該当期間に退避されたレコードを削除します
DELETE FROM raw.library_flac_history
WHERE changed_at >= '2026-07-01 00:00:00'; -- ※実際の実行日時に合わせて調整してくださいませ

-- 2. メインテーブルから、該当期間に新規登録・更新されたレコードを削除します
DELETE FROM raw.library_flac
WHERE analyzed_at >= '2026-07-01 00:00:00';

COMMIT;
